const accountId = 34
// const is used to declare constants that cannot be changed once declared

let accountEmail = "dpl@gmail.com"
// let keyword is used for declaring the variable, the value of variable is changable

var accountPassword = 7576
// var is also used for declaring the variable, 
// but it is prefered not to use var because of problem
// regarding block scope and functional scope

accountCity = "maharashtra"
// we can also declare a varibale without using let or var

console.table([accountId, accountEmail, accountPassword, accountCity])
